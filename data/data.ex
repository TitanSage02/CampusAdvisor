### Université d’Abomey-Calavi

**Faculté des Lettres, Langues, Arts et Communication (FLLAC)**

La Faculté des Lettres, Langues, Arts et Communication (FLLAC) propose plusieurs filières :

1. **Allemand**  
   Accessible par classement, avec les baccalauréats recommandés A1, A2 et B. Les matières principales sont l'allemand (LV1), l'anglais (LV2)/histoire-géographie (B) et le français. Les débouchés incluent l'interprétariat et le tourisme.
2. **Anglais**  
   Accessible par classement, avec les baccalauréats recommandés A1, A2 et B. Les matières principales sont l'anglais (LV1), l'anglais (LV2)/histoire-géographie (B) et le français. Les débouchés incluent l'interprétariat, le tourisme, l'enseignement dans les collèges et lycées, et le travail comme lecteur correcteur dans les maisons d'édition.
3. **Espagnol**  
   Accessible par classement, avec les baccalauréats recommandés A1, A2, B, C et D. Les matières principales sont l'espagnol (LV1), l'anglais (LV2)/histoire-géographie (B) et le français. Les débouchés incluent l'interprétariat et le tourisme.
4. **Lettres Modernes**  
   Accessible par classement, avec les baccalauréats recommandés A1, A2, B, C et D. Les matières principales sont le français et l'histoire-géographie. Les débouchés incluent l'enseignement dans les collèges et lycées, et le travail comme lecteur correcteur dans les maisons d'édition.
5. **Sciences du Langage et de la Communication**  
   Accessible par classement, avec les baccalauréats recommandés A1, A2, B, C, D et G. Les matières principales sont le français, l'anglais (LV1) et la philosophie/étude de cas (G). Les débouchés incluent la didactique des langues française et africaines, et le conseil en éducation bi/plurilingue et interculturelle.

