### Université d'Abomey-Calavi (UAC)

**1. Faculté de Droit et de Science Politique (FADESP)**

La FADESP offre une formation en Droit accessible par classement aux bacheliers des séries A1, A2, B, C, D et G1. Les matières principales à étudier sont le Français, l'Histoire-Géographie, la Philosophie et les Mathématiques. Les débouchés sont vastes : magistrature, avocature, notariat, administration publique, etc.

**2. Faculté des Sciences Economiques et de Gestion (FASEG)**

La FASEG propose plusieurs filières accessibles par classement :

* **Sciences Economiques et de Gestion (SEG) :** Ouverte aux bacheliers des séries A1, A2, B, C, D, G1, G2 et G3, elle requiert de bonnes connaissances en Français, Mathématiques, Histoire-Géographie et Economie. Les débouchés incluent des postes dans la banque, la finance, la gestion d'entreprise, etc.
* **Sciences de Gestion et Administration des Entreprises (SGAE) :** Accessible avec les mêmes séries que la SEG, elle met l'accent sur la gestion et l'administration. Les débouchés sont similaires à ceux de la SEG, avec une orientation plus prononcée vers l'entrepreneuriat.
* **Comptabilité et Audit (CA) :** Ouverte aux séries A1, A2, B, C, D, G1, G2 et G3, cette filière requiert une solide base en Mathématiques. Les débouchés incluent des postes de comptable, auditeur, contrôleur de gestion, etc.

**3. Faculté des Lettres, Arts et Sciences Humaines (FLASH)**

La FLASH propose une variété de formations en lettres, langues, arts et sciences humaines, accessibles par classement :

* **Lettres Modernes (LM) :** Ouverte aux bacheliers des séries A1, A2, C et D, elle requiert de solides compétences en Français et en langues étrangères. Les débouchés incluent l'enseignement, la recherche, la communication, etc.
* **Histoire et Archéologie (HA) :** Accessible avec les mêmes séries que LM, cette filière met l'accent sur l'histoire et l'archéologie. Les débouchés incluent la recherche, l'enseignement, le tourisme culturel, etc.
* **Géographie et Aménagement du Territoire (GAT) :** Ouverte aux séries A1, A2, B, C, D et DT/STI, elle requiert de bonnes connaissances en géographie, mathématiques et sciences de la terre. Les débouchés incluent l'aménagement du territoire, la cartographie, l'environnement, etc.
* **Sociologie (SOCIO) :** Accessible avec les mêmes séries que LM, cette filière explore les dynamiques sociales. Les débouchés incluent la recherche, le travail social, les ressources humaines, etc.
* **Philosophie (PHILO) :** Ouverte aux séries A1, A2, C et D, elle requiert une grande capacité d'analyse et de réflexion. Les débouchés incluent l'enseignement, la recherche, le journalisme, etc.
* **Anglais (ANG) :** Accessible avec les mêmes séries que LM, cette filière forme des spécialistes de la langue anglaise. Les débouchés incluent l'enseignement, la traduction, l'interprétariat, etc.
* **Allemand (ALL) :** Ouverte aux séries A1, A2, C et D, cette filière forme des spécialistes de la langue allemande. Les débouchés incluent l'enseignement, la traduction, l'interprétariat, etc.
* **Espagnol (ESP) :** Accessible avec les mêmes séries que LM, cette filière forme des spécialistes de la langue espagnole. Les débouchés incluent l'enseignement, la traduction, l'interprétariat, etc.

**4. Faculté des Sciences Agronomiques (FSA)**

La FSA propose des formations en agronomie, accessible par classement :

* **Agronomie (AGRO) :** Ouverte aux bacheliers des séries C, D et DT/STI, elle requiert de bonnes connaissances en biologie, chimie et mathématiques. Les débouchés incluent l'agriculture, l'agroalimentaire, l'environnement, etc.
* **Sciences et Techniques de l'Eau et de l'Environnement (STEE) :** Accessible avec les mêmes séries que AGRO, cette filière se focalise sur la gestion de l'eau et de l'environnement. Les débouchés incluent l'hydraulique, l'assainissement, la protection de l'environnement, etc.

**5. Faculté des Sciences et Techniques (FAST)**

La FAST propose des formations en sciences et technologies, accessible par classement :

* **Mathématiques (MATHS) :** Ouverte aux bacheliers des séries C et D, elle requiert de solides compétences en mathématiques. Les débouchés incluent l'enseignement, la recherche, l'informatique, etc.
* **Physique (PHYS) :** Accessible avec les mêmes séries que MATHS, cette filière explore les lois de la physique. Les débouchés incluent l'enseignement, la recherche, l'industrie, etc.
* **Chimie (CHIM) :** Ouverte aux séries C et D, cette filière explore la composition de la matière. Les débouchés incluent l'enseignement, la recherche, l'industrie chimique, etc.
* **Biologie (BIO) :** Accessible avec les mêmes séries que MATHS, cette filière explore le monde vivant. Les débouchés incluent l'enseignement, la recherche, la santé, etc.
* **Informatique (INFO) :** Ouverte aux séries C, D et DT/STI, cette filière forme des spécialistes de l'informatique. Les débouchés incluent le développement web, la programmation, l'administration système, etc.

**6. Ecole Nationale d'Administration et de Magistrature (ENAM)**

L'ENAM forme les hauts fonctionnaires de l'Etat béninois. L'accès se fait par concours.

**7. FLASH - Adjarra**

La Faculté de Lettres, Arts et Sciences Humaines-Adjarra (FLASH-Adjarra) est une Faculté de l'Université d'Abomey Calavi. Elle propose plusieurs filières :

* **Géographie et Aménagement du Territoire à la FLASH-Adjarra :** Accessible par classement, avec les baccalauréats recommandés suivants : A, B, C, D et DT/STI. Les matières principales sont le Français, l'Histoire-Géographie/Anglais (DT/STI), et les Mathématiques. Les débouchés incluent l'enseignement, le travail dans les laboratoires et institutions de recherche, ainsi que l'assainissement.
* **Socio-Anthropologie :** Egalement accessible par classement. Les baccalauréats recommandés sont A1, A2, B, C, D, DEAT (toutes spécialités) et DT/STI. Les matières à étudier incluent le Français, l'Histoire-Géographie/Maths (DT/STI), l'Anglais (LV1) et, pour le DEAT, toutes les trois matières écrites. Les débouchés incluent des postes dans les centres sociaux, les ministères et la recherche.
* **Anglais :** Accessible par classement, avec les baccalauréats recommandés A1, A2, B, C et D. Les matières principales sont l'Anglais (LV1), l'Anglais (LV2)/Histoire-Géographie. Les débouchés incluent l'interprétariat, le tourisme et l'enseignement.

